#include <stdio.h>

int main() {

    int a = 30, b = 7;
    // using operators and printing results
    printf("-----------------a + b = %d\n", a + b);
    printf("-----------------a - b = %d\n", a - b);
    printf("-----------------a * b = %d\n", a * b);
    printf("-----------------a / b = %d\n", a / b);
    printf("-----------------a % b = %d\n", a % b);
    printf("-----------------+a = %d\n", +a);
    printf("----------------- -a = %d\n", -a);
    printf("-----------------a++ = %d\n", a++);
    printf("-----------------a-- = %d\n", a--);
    printf("-----------------a = %d\n", a);
    printf("-----------------++a = %d\n", ++a);
    printf("----------------- --a = %d\n", --a);

    return 0;
}
